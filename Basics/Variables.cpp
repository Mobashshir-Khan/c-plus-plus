/*              VARIABLES
-->Containers to store numbers or values

                DATA TYPES
-->int = to store numeric values
-->float = to store decimal value numbers
-->char = to store character or alphabet values
-->double = increases the capacity of float
-->long = increses the capacity of int

                SYNTAX
-->datatype variable_name = value

        VARIABLE DECLARING RULES
-->Start from alphabet or underscore or dollar.
-->Special characters are not allowed.
-->Keywords are not allowed.
-->Commas or blanks are not allowed.
*/

#include <iostream>
using namespace std;
int main()
{
    int a;
    float b;
    char c;
    a = 4;
    b = 3.14;
    c = 's';
    cout << a << endl;
    cout << b << endl;
    cout << c << endl;
    return 0;
}
